package model;

/**
 *
 * @author Admin
 */
public class Department {
    int id;
    String describe;

    public Department() {
    }

    public Department(int id, String describe) {
        this.id = id;
        this.describe = describe;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getDescribe() {
        return describe;
    }

    public void setDescribe(String describe) {
        this.describe = describe;
    }

    @Override
    public String toString() {
        return "Department{" + "id=" + id + ", describe=" + describe + '}';
    }
    
}
